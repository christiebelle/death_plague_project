import React from "react";
import GoogleMapReact from "google-map-react";
import Locations from "../db/locations.js"


class Map extends React.Component {
  constructor(props){
    super(props);
    this.map = null;
    this.maps = null;
    this.state = {
      markers: Locations
    };

    this.storeMaps = this.storeMaps.bind(this);
  }

  storeMaps({map, maps}){
    this.map = map;
    this.maps = maps;
    let markers = this.state.markers
    for (let marker of markers){
      this.renderMarker(marker)
    }
  }

  renderMarker(point){
    let infoWindow = new this.maps.InfoWindow({
      content: point.text
    });

    let marker = new this.maps.Marker({
      position:  {lat:point.lat, lng:point.lng},
      map: this.map
    });

    marker.addListener("mouseover", (event) => {
      infoWindow.open(this.map, marker);
    });

    marker.addListener('mouseout', () => {
    infoWindow.close();
});

  }


  render(){
    return (
      <div style={{height: "300px", width: "100%"}}>
        <h3>Scroll across to Europe and follows the Black Deaths Journey</h3>
        <p>Hover over the markers for some interesting facts abiut key locations</p>
        <GoogleMapReact
          center = {{lat: 40.142, lng: 94.661}}
          zoom = {3}
          options = {{styles: exampleMapStyles,
            disableDefaultUI: true }}
            onGoogleApiLoaded={this.storeMaps}
            yesIWantToUseGoogleMapApiInternals={true}

          />
        </div>
      );
    }
  }


